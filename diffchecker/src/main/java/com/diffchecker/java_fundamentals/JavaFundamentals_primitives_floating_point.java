package com.diffchecker.java_fundamentals;

public class JavaFundamentals_primitives_floating_point {
  public static void main(String[] args) {

    float f = 13.14654623f; // must have the letter f
    double d = 16.14546235423545356d; // must have the letter d
    char c = '&'; // single quotes
    boolean b = true; // not enclosed in quotes

    System.out.println("float = " + f);
    System.out.println("double = " + d);
    System.out.println("char = " + c);
    System.out.println("boolean = " + b);
  }
}
/*
 * PRIMITIVE DATA TYPES: (only one value at a time)
 * - Integer (byte, short, int, long)
 * - Floating Point (float, double)
 * - Boolean (true, false)
 * - Character
 * 
 * NON-PRIMITIVE TYPES: (can hold multiple values)
 * - String
 * - Array
 * - List
 * - Map
 * - Set
 * - Object
 * 
 */